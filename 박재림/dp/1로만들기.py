n = int(input())  # 사용자 입력 받기

# DP 테이블 초기화 (최대 n까지 저장)
dp = [0] * (n + 1)  # dp[i]는 i를 1로 만드는 최소 연산 횟수를 저장

# 2부터 n까지 최소 연산 횟수 구하기
for i in range(2, n + 1):
    dp[i] = dp[i - 1] + 1  # 기본적으로 -1 하는 경우

    if i % 2 == 0:  # 2로 나누어질 때 최소 연산 횟수 갱신
        dp[i] = min(dp[i], dp[i // 2] + 1)

    if i % 3 == 0:  # 3으로 나누어질 때 최소 연산 횟수 갱신
        dp[i] = min(dp[i], dp[i // 3] + 1)

# n을 1로 만드는 최소 연산 횟수 출력
print(dp[n])
