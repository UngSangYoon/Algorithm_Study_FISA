'''
잃어버린 괄호

문제
세준이는 양수와 +, -, 그리고 괄호를 가지고 식을 만들었다.
그리고 나서 세준이는 괄호를 모두 지웠다.

그리고 나서 세준이는 괄호를 적절히 쳐서

이 식의 값을 최소로 만들려고 한다.

괄호를 적절히 쳐서 이 식의 값을 최소로 만드는 프로그램을 작성하시오.

입력

첫째 줄에 식이 주어진다. 
식은 ‘0’~‘9’, ‘+’,
그리고 ‘-’만으로 이루어져 있고,

가장 처음과 마지막 문자는 숫자이다. 
그리고 연속해서 두 개 이상의 연산자가 나타나지 않고, 
5자리보다 많이 연속되는 숫자는 없다. 
수는 0으로 시작할 수 있다. 
입력으로 주어지는 식의 길이는 50보다 작거나 같다.

출력
첫째 줄에 정답을 출력한다.
'''

str_ = input()

# 문자열을 '-' 연산자를 기준으로 파싱하여, 리스트로 만듭니다.
# 예: "55-50+40" -> ['55', '50+40']
numbers = str_.split('-')

# 첫 번째 그룹은 그대로 '+' 기준으로 나눈 후 합산합니다.
# 예: '55' -> [55]
result = sum(map(int, numbers[0].split('+')))

# 두 번째 그룹부터는 각각 '+' 기준으로 나눈 후 합산한 값을 빼줍니다.
# 예: '50+40' -> 50 + 40 = 90, 따라서 최종 결과는 55 - 90 = -35
for group in numbers[1:]:
    result -= sum(map(int, group.split('+')))

print(result)