# 1, 2, 3의 합으로 정수 나타내기
# 1 -> 1
# 2 -> 1+1, 2 -> 2
# 3 -> 1+1+1, 1+2, 2+1, 3 -> 4
# 4 -> 1+1+1+1, 1+1+2, 1+2+1+1, 2+1+1+1, 2+2, 1+3, 3+1 -> 7
# 5 -> 11111, 1112, 1121, 1211, 2111, 113, 131, 311, 122, 212, 221, 23, 32 -> 13
# 6 -> 24
# 7 -> 44

T = int(input())
n = [int(input()) for _ in range(T)]


f = [0] * 11
f[1] = 1
f[2] = 2
f[3] = 4

for i in range(4, 11):
    f[i] = f[i-1] + f[i-2] + f[i-3]

for k in n:
    print(f[k])